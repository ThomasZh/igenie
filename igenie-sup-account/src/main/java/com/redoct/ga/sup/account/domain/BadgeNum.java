package com.redoct.ga.sup.account.domain;

public class BadgeNum {
	private String accountId;
	private int totalNum;
	private int symbolNum;
	private int profileNum;
	private int friendNum;
	private int activityTotalNum;
	private int activityMineNum;
	private int activityFriendNum;
	private int activityRecommendNum;

	public String getAccountId() {
		return accountId;
	}

	public void setAccountId(String accountId) {
		this.accountId = accountId;
	}

	public int getTotalNum() {
		return totalNum;
	}

	public void setTotalNum(int totalNum) {
		this.totalNum = totalNum;
	}

	public int getSymbolNum() {
		return symbolNum;
	}

	public void setSymbolNum(int symbolNum) {
		this.symbolNum = symbolNum;
	}

	public int getProfileNum() {
		return profileNum;
	}

	public void setProfileNum(int profileNum) {
		this.profileNum = profileNum;
	}

	public int getFriendNum() {
		return friendNum;
	}

	public void setFriendNum(int friendNum) {
		this.friendNum = friendNum;
	}

	public int getActivityTotalNum() {
		return activityTotalNum;
	}

	public void setActivityTotalNum(int activityTotalNum) {
		this.activityTotalNum = activityTotalNum;
	}

	public int getActivityMineNum() {
		return activityMineNum;
	}

	public void setActivityMineNum(int activityMineNum) {
		this.activityMineNum = activityMineNum;
	}

	public int getActivityFriendNum() {
		return activityFriendNum;
	}

	public void setActivityFriendNum(int activityFriendNum) {
		this.activityFriendNum = activityFriendNum;
	}

	public int getActivityRecommendNum() {
		return activityRecommendNum;
	}

	public void setActivityRecommendNum(int activityRecommendNum) {
		this.activityRecommendNum = activityRecommendNum;
	}

}
