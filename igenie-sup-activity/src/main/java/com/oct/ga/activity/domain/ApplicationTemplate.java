package com.oct.ga.activity.domain;

public class ApplicationTemplate {
	private String activiyId;
	private String contactTemp;
	private String companionTemp;

	public String getActiviyId() {
		return activiyId;
	}

	public void setActiviyId(String activiyId) {
		this.activiyId = activiyId;
	}

	public String getContactTemp() {
		return contactTemp;
	}

	public void setContactTemp(String contactTemp) {
		this.contactTemp = contactTemp;
	}

	public String getCompanionTemp() {
		return companionTemp;
	}

	public void setCompanionTemp(String companionTemp) {
		this.companionTemp = companionTemp;
	}

}